    public void find(String attri,String mnv){
        String[] col={"Mã nhân viên","Tên nhân viên","Địa chỉ","Email","SĐT","Mã tài khoản","Lương"};
        DefaultTableModel dtm=new DefaultTableModel(col,0);
        NhanvienBUS a=new NhanvienBUS();
        ArrayList<NhanvienDTO> arr=new ArrayList<NhanvienDTO>();

        switch(attri){
            case "Mã nhân viên":
                arr=a.findnv("MaNhanVien", mnv);
                break;
            case "Tên nhân viên":
                arr=a.findnv("TenNhanVien", mnv);
                break;
        }
        temp.clear();
        temp.addAll(arr);
        NhanvienDTO nv= new NhanvienDTO();
        for(int i=0;i<arr.size();i++){
            nv=arr.get(i);
            String manv=nv.getmanv();
            String tennv=nv.gettennv();
            String address=nv.getaddress();
            String email=nv.getemail();
            String sdt=nv.getsdt();
            String matk=nv.getmatk();
            String luong=nv.getluong();
            Object[] row={manv,tennv,address,email,sdt,matk,luong};
            dtm.addRow(row);
        }
        list.setModel(dtm);
        
    }
