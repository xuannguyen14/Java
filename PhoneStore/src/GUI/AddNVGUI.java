    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if(manv.getText().equals("")||tennv.getText().equals("")||address.getText().equals("")||emailname.getText().equals("")||sdt.getText().equals("")||luong.getText().equals(""))
       {
           JOptionPane.showMessageDialog(rootPane,"Xin vui lòng điền đầy đủ thông tin");
       }
       else{
        NhanvienDTO nv=new NhanvienDTO();
        NhanvienBUS a= new NhanvienBUS();
        nv.setmanv(manv.getText());
        nv.settennv(tennv.getText());
        nv.setaddress(address.getText());
        nv.setemail(emailname.getText()+emailtype.getSelectedItem().toString());
        nv.setsdt(sdt.getText());
        nv.setmatk(matk.getText());
        nv.setluong(luong.getText());
        a.addnv(nv);
        JOptionPane.showMessageDialog(rootPane,"Thành công");
        this.dispose();
        }
    }//GEN-LAST:event_addActionPerformed

