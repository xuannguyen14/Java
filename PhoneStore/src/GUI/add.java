    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        DateFormat a=new SimpleDateFormat("yyyy-MM-dd");
        Date s=start.getDate();
        Date e=end.getDate();
        String s1=a.format(s).trim();
        String s2=a.format(e).trim();
        if(makm.getText().equals("")||masp.getText().equals("")||tenkm.equals("")||tile.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Xin vui lòng điền đầy đủ");
        }
        else if(!isNumberic(tile.getText())){
            JOptionPane.showMessageDialog(rootPane,"Định dạng phải là một số");
        }
        else if(s.compareTo(e)>0){
            JOptionPane.showMessageDialog(rootPane,"Ngày bắt đầu và kết thúc không hợp lệ");
        }
        else
        {
        KhuyenmaiDTO km=new KhuyenmaiDTO();
        KhuyenmaiBUS add= new KhuyenmaiBUS();
        km.setmakm(makm.getText().trim());
        km.setmasp(masp.getText().trim());
        km.settile(Integer.parseInt(tile.getText().trim()));
        km.settenkm(tenkm.getText().trim());
        km.setngaybd(s1);
        km.setngaykt(s2);
        add.addkm(km);
        this.dispose();
        JOptionPane.showMessageDialog(rootPane,"Thành công");
        }
    }//GEN-LAST:event_addActionPerformed
    private static boolean isNumberic(String s){
        try{
            Integer.parseInt(s);
            return true;
        }
        catch(NumberFormatException e){
            return false;
        }
    }
