    private void loadData() {
        model = new MyTableModel().setTable(listColumn, khachHangBUS.getDanhSachKhachHang());
        khachHangTable.setModel(model);
        
    }
    
    private void loadData(Vector<Vector> data) {
        model = new MyTableModel().setTable(listColumn, data);
        khachHangTable.setModel(model);
    }
    
    private void showMessage(String message) {
        JOptionPane.showMessageDialog(this, message);
    }
    
    private Vector getKhachHangIsSelected() {
        int rowIsSelected = khachHangTable.getSelectedRow();
        if (rowIsSelected < 0) {
            return null;
        }
        Vector khachHangIsSelected = new Vector();
        khachHangIsSelected.add(model.getValueAt(rowIsSelected, 0));
        khachHangIsSelected.add(model.getValueAt(rowIsSelected, 1));
        khachHangIsSelected.add(model.getValueAt(rowIsSelected, 2));
        khachHangIsSelected.add(model.getValueAt(rowIsSelected, 3));
        khachHangIsSelected.add(model.getValueAt(rowIsSelected, 4));
        
        return khachHangIsSelected;
    }
    
    private void xemChiTiet() {
        Vector khachHangIsSelected = getKhachHangIsSelected();
        if (khachHangIsSelected == null) {
            showMessage("Chọn khách hàng cần xem!");
            return;
        }
        dispose();
        chiTietKhachHangGUI.run(this, khachHangBUS, khachHangIsSelected);
    }
    
    private void suaKhachHang() {
        Vector khachHangIsSelected = getKhachHangIsSelected();
        if (khachHangIsSelected == null) {
            showMessage("Chọn khách hàng cần xóa!");
            return;
        }
        dispose();
        suaKhachHangGUI.run(this, khachHangBUS, khachHangIsSelected);
    }
    
    private void themKhachHang() {
        dispose();
        themKhachHangGUI.run(this, khachHangBUS);
    }
    
    private void xoaKhachHang() {
        Vector khachHangIsSelected = getKhachHangIsSelected();
        if (khachHangIsSelected == null) {
            showMessage("Chọn khách hàng cần xóa!");
            return;
        }
        khachHangBUS.removeKhachHang(khachHangIsSelected);
        loadData();
    }
    
    private void timKiem() {
        String text = searchTextField.getText();
        Vector<Vector> ketQua = null;
        ketQua = khachHangBUS.getKhachHangByMaKhachHang(text);
        if (ketQua.size() != 0) {
            loadData(ketQua);
            System.out.println(ketQua.size());
            return;
        }
        ketQua = khachHangBUS.getDanhSachKhachHangByTen(text);
        if (ketQua.size() != 0) {
            loadData(ketQua);
            return;
        }
        ketQua = khachHangBUS.getDanhSachKhachHangByDiaChi(text);
        if (ketQua.size() != 0) {
            loadData(ketQua);
            return;
        }
        ketQua = khachHangBUS.getDanhSachKhachHangByEmail(text);
        if (ketQua.size() != 0) {
            loadData(ketQua);
            return;
        }
        ketQua = khachHangBUS.getDanhSachKhachHangBySDT(text);
        if (ketQua.size() != 0) {
            loadData(ketQua);
            return;
        }
    }
    
