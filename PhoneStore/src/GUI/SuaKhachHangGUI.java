    private Vector getData() {
        Vector khacHangMoi = new Vector();
        khacHangMoi.add(khachHangCu.get(0).toString());
        khacHangMoi.add(tenKHTextField.getText());
        khacHangMoi.add(diaChiKHTextField.getText());
        khacHangMoi.add(emailKHTextField.getText());
        khacHangMoi.add(SDTKHTextField.getText());
        return khacHangMoi;
    }
    
    private void clearData() {
        tenKHTextField.setText("");
        diaChiKHTextField.setText("");
        emailKHTextField.setText("");
        SDTKHTextField.setText("");
    }
    
    private void loadData() {
        tenKHTextField.setText(khachHangCu.get(1).toString());
        diaChiKHTextField.setText(khachHangCu.get(2).toString());
        emailKHTextField.setText(khachHangCu.get(3).toString());
        SDTKHTextField.setText(khachHangCu.get(4).toString());
    }
    
    public void run(KhachHangGUI root1, KhachHangBUS khachHangBUS, Vector khachHangCu) {
        this.khachHangBUS = khachHangBUS;
        this.root1 = root1;
        this.khachHangCu = khachHangCu;
        this.choice = choice;
        loadData();
        setVisible(true);
    }
    
    public void run(ChiTietKhachHangGUI root2, KhachHangBUS khachHangBUS, Vector khachHangCu) {
        this.khachHangBUS = khachHangBUS;
        this.root2 = root2;
        this.khachHangCu = khachHangCu;
        this.choice = choice;
        loadData();
        setVisible(true);
    }
    
    private void showMessage(String message) {
        JOptionPane.showMessageDialog(this, message);
    }
    
    
    private boolean checkNull() {
        if (tenKHTextField.getText().length() == 0) {
            return true;
        }
        if (diaChiKHTextField.getText().length() == 0) {
            return true;
        }
        if (emailKHTextField.getText().length() == 0) {
            return true;
        }
        if (SDTKHTextField.getText().length() == 0) {
            return true;
        }
        return false;
    }
    
