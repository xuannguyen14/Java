    private void tblTaikhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTaikhoanMouseClicked
        // TODO add your handling code here:
        int i = tblTaikhoan.getSelectedRow();
        int[] y = tblTaikhoan.getSelectedColumns();
        if(i >= 0){
            TaikhoanDTO taikhoan = new TaikhoanDTO();
            
            taikhoan = taikhoanBUS.getTaikhoanByMaTK((String) tblTaikhoan.getValueAt(i, y[0]));//y[0] do cột mã tài khoản ở cột 0
            txtMaTK.setText(taikhoan.getMaTK());
            txtPassword.setText(taikhoan.getPassword());
        }
    }//GEN-LAST:event_tblTaikhoanMouseClicked

