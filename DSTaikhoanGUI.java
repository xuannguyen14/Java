    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:

        int row = tblTaikhoan.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(DSTaikhoanGUI.this, "Vui lòng chọn tài khoản trước", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(DSTaikhoanGUI.this,"Bạn có chắc chắn muốn xóa không?");

            if(confirm == JOptionPane.YES_OPTION){
                String maHD = String.valueOf(tblTaikhoan.getValueAt(row, 0));

                taikhoanBUS.deleteTaikhoan(maHD);

                defaultTableModel.setRowCount(0);
                setTableData(taikhoanBUS.getAllTaikhoan());
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

